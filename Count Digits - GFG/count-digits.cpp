class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int ans;
        int r=0;
        int org_n=N;
        while(N>0)
        {
            r=N%10;
            N=N/10;
            if(r!=0&&org_n%r==0)
            {
                ans++;
            }
        }
        return ans;
    }
};

